    private void CekLogin() {
        try {
            if (txtusername.getText().equals("") || txtpass.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Data Tidak Boleh Kosong", "Pesan", JOptionPane.ERROR_MESSAGE);
                txtusername.requestFocus();
                hapuslayar();
            } else {
                st = conn.createStatement();
                String sql = ("SELECT * FROM login WHERE username = '" + txtusername.getText() + "' AND password = " + "'" + String.valueOf(txtpass.getText()) + "'");
                ResultSet rs = st.executeQuery(sql);
                if (rs.next()) {
                    this.dispose();
                    new Main().setVisible(true);

                } else {
                    JOptionPane.showMessageDialog(rootPane, "Username dan Password Salah\nAtau Akun Belum Terdaftar", "Pesan",
                            JOptionPane.ERROR_MESSAGE);
                    hapuslayar();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Gagal");
        }
    }

    private void hapuslayar() {
        txtusername.setText("");
        password.setText("");
        txtusername.setEnabled(true);
        txtpass.setEnabled(true);
    }

