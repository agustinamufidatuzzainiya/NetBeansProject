    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
     int kodembl = Integer.parseInt(TxtKdMbl.getText());
     lamambl = Integer.parseInt(TxtLama.getText());
        
      if (kodembl == 1111) {
           TxtMerek.setText("Honda");
           HrgMbl = 500000;
           totalbayar = HrgMbl * lamambl;
           TxtHarga.setText(String.valueOf(HrgMbl));
           TxtLama.setText(String.valueOf(lamambl));
           TxtTotal.setText(Integer.toString(totalbayar));
           
      }
      else
           if (kodembl == 2222) {
           TxtMerek.setText("Avanza");
           HrgMbl = 400000;
           totalbayar = HrgMbl * lamambl;
           TxtHarga.setText(String.valueOf(HrgMbl));
           TxtLama.setText(String.valueOf(lamambl));
           TxtTotal.setText(Integer.toString(totalbayar));
           
      }
          else
          if (kodembl == 3333) {
           TxtMerek.setText("Xenia");
           HrgMbl = 300000;
           totalbayar = HrgMbl * lamambl;
           TxtHarga.setText(String.valueOf(HrgMbl));
           TxtLama.setText(String.valueOf(lamambl));
           TxtTotal.setText(Integer.toString(totalbayar));
           
      }
       else
          if (kodembl == 4444) {
           TxtMerek.setText("Mitshubisi");
           HrgMbl = 200000;
           totalbayar = HrgMbl * lamambl;
           TxtHarga.setText(String.valueOf(HrgMbl));
           TxtLama.setText(String.valueOf(lamambl));
           TxtTotal.setText(Integer.toString(totalbayar));
           
      }
       else
          if (kodembl == 5555) {
           TxtMerek.setText("Kijang");
           HrgMbl = 100000;
           totalbayar = HrgMbl * lamambl;
           TxtHarga.setText(String.valueOf(HrgMbl));
           TxtLama.setText(String.valueOf(lamambl));
           TxtTotal.setText(Integer.toString(totalbayar));
           
      }

        double totalbyr = (HrgMbl * lamambl) ;
        TxtTotal.setText(String.valueOf(totalbyr));
    }//GEN-LAST:event_jButton2ActionPerformed

