    private void BtnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHitungActionPerformed
    //   if (txtNama.getText().isEmpty() || txtUTS.getText().isEmpty() || txtTM.getText().isEmpty())
  //            getToolkit().beep();
   //   JOptionPane.showMessageDialog(null,"Data Tidak Boleh kosong");
        
      double uts = Double.parseDouble(txtUTS.getText());
      double tm = Double.parseDouble(txtTM.getText());
      double uas = Double.parseDouble(txtUAS.getText());
      
      double nilairata = (uts + tm + uas) / 3 ;
      txtNpm.getText();
      lblNama.setText(txtNpm.getText());
      LblNilairata.setText(nilairata + "   Nalai Rata - RataNya");
      
      if (nilairata >= 90) {
          LblGrade.setText("A");
          lblHasil.setText("Lulus");
      }
      else
          if (nilairata >= 80) {
              LblGrade.setText("B");
              lblHasil.setText("Lulus Tapi Nilai B");
              
          }
      else
              if(nilairata >= 70){
                  LblGrade.setText("C");
                  lblHasil.setText("Lulus Tapi Nilai C");
              }
      else
              {
                  LblGrade.setText("D");
                  lblHasil.setText("Tidak Lulus");
              }
         
    }//GEN-LAST:event_BtnHitungActionPerformed

