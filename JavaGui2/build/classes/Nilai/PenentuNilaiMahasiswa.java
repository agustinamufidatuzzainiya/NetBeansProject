    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        
        try{
            
          BufferedWriter out = new BufferedWriter(new FileWriter(" Nilaikui.txt") );
          out.write(txtNpm.getText());
          out.write(txtTM.getText());
          out.write(txtUAS.getText());
          out.write(txtUTS.getText());
          out.write(lblNama.getText());
          out.write(lblHasil.getText());
          out.write(LblGrade.getText());
          out.write(LblNilairata.getText());  
          
          JOptionPane.showMessageDialog(null, "Berhasil Disimpan Filenya");
          out.close();
        }catch (Exception e) {
            System.err.println("Error " +e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

