    private void btnsamadenganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsamadenganActionPerformed
       switch(pilih)
            {
                case 1:
                angka2 = Double.parseDouble(txthasil.getText());
                total = angka1 + angka2;
                angka = Double.toString(total);
                txthasil.setText(angka);
                toogle=false;
                break;
                case 2:
                angka2 = Double.parseDouble(txthasil.getText());
                total = angka1 - angka2;
                angka = Double.toString(total);
                txthasil.setText(angka);
                toogle=false;
                break;
                case 3:
                angka2 = Double.parseDouble(txthasil.getText());
                total = angka1 * angka2;
                angka = Double.toString(total);
                txthasil.setText(angka);
                toogle=false;
                break;
                case 4:
                angka2 = Double.parseDouble(txthasil.getText());
                total = angka1 / angka2;
                angka = Double.toString(total);
                txthasil.setText(angka);
                toogle=false;
                break;
                case 5:
                angka2 = Double.parseDouble(txthasil.getText());
                total = angka1 % angka2;
                angka = Double.toString(total);
                txthasil.setText(angka);
                toogle=false;
                break;
                default:
                    break;
        }
    }//GEN-LAST:event_btnsamadenganActionPerformed

